# Operasi Aritmatika
a = 11
b = 3


# Operasi penjumlahan +
result  = a + b
print(a, '+', b, '=', result)


# Operasi pengurangan -
result  = a - b
print(a, '-', b, '=', result)


# Operasi perkalian *
result  = a * b
print(a, '*', b, '=', result)


# Operasi pembagian / 
result  = a / b
print(a, ':', b, '=', result)


# Operasi eksponen (pangkat) **
result  = a ** b
print(a, '**', b, '=', result)


# Operasi modulus (sisa pembagian) %
result  = a % b
print(a, '%', b, '=', result)


# Operasi floor division //
result  = a // b
print(a, '//', b, '=', result)